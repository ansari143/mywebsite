export type Option = { id: "A" | "B" | "C" | "D"; text: string };
export type Question = {
  id: string;
  text: string;
  options: Option[];
  correct: Option["id"];
  topic?: string;
};

export type CareerTest = {
  slug: string;
  title: string;
  icon: string;
  summary: string;
  bestFor: string[];
  durationMin: number;
  questions: Question[];
};

function q(
  topic: string,
  text: string,
  A: string,
  B: string,
  C: string,
  D: string,
  correct: "A" | "B" | "C" | "D"
): Omit<Question, "id"> {
  return {
    topic,
    text,
    options: [
      { id: "A", text: A },
      { id: "B", text: B },
      { id: "C", text: C },
      { id: "D", text: D },
    ],
    correct,
  };
}

function make50(slug: string, pool: Omit<Question, "id">[]): Question[] {
  // Real 50: pool should already be 50 items.
  return pool.slice(0, 50).map((qq, idx) => ({ ...qq, id: `${slug}-q${idx + 1}` }));
}

