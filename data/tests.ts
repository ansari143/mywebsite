function autoHindi(input: string): string {
  // Keep numbers/symbols; translate word-by-word lightly.
  // If a word is not found, keep it as-is.
  const tokens = input.split(/(\s+|[,:?()°./+\-→])/g); // keep separators
  const out = tokens.map((t) => {
    // Skip separators
    if (/^\s+$/.test(t) || /^[,:?()°./+\-→]$/.test(t)) return t;

    // Handle curly apostrophe variants etc.
    const clean = t.replace(/[’']/g, "'");
    // Exact match
    if (dict[clean]) return dict[clean];

    // Try title-case removal for punctuation
    const stripped = clean.replace(/^[“"']+|[”"']+$/g, "");
    if (dict[stripped]) {
      // reattach quotes if needed
      const prefix = clean.match(/^[“"']+/)?.[0] ?? "";
      const suffix = clean.match(/[”"']+$/)?.[0] ?? "";
      return `${prefix}${dict[stripped]}${suffix}`;
    }

    return t;
  });

  // cleanup double spaces
  return out.join("").replace(/\s{2,}/g, " ").trim();
}

// =================== HELPERS ===================

function q(
  topic: string,
  text: string,
  A: string,
  B: string,
  C: string,
  D: string,
  correct: "A" | "B" | "C" | "D"
): Omit<Question, "id"> {
  return {
    topic,
    text,
    hi: autoHindi(text), // ✅ auto Hindi
    options: [
      { id: "A", text: A, hi: autoHindi(A) },
      { id: "B", text: B, hi: autoHindi(B) },
      { id: "C", text: C, hi: autoHindi(C) },
      { id: "D", text: D, hi: autoHindi(D) },
    ],
    correct,
  };
}

